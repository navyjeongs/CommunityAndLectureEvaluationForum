package freeForum;

public class freeForum {
	
	private int freeForumID;
	private String freeForumTitle;
	private String userID;
	private String freeForumDate;
	private String freeForumContent;
	private int freeForumAvailable;
	
	public int getfreeForumID() {
		return freeForumID;
	}
	public void setfreeForumID(int freeForumID) {
		this.freeForumID = freeForumID;
	}
	public String getfreeForumTitle() {
		return freeForumTitle;
	}
	public void setfreeForumTitle(String freeForumTitle) {
		this.freeForumTitle = freeForumTitle;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getfreeForumDate() {
		return freeForumDate;
	}
	public void setfreeForumDate(String freeForumDate) {
		this.freeForumDate = freeForumDate;
	}
	public String getfreeForumContent() {
		return freeForumContent;
	}
	public void setfreeForumContent(String freeForumContent) {
		this.freeForumContent = freeForumContent;
	}
	public int getfreeForumAvailable() {
		return freeForumAvailable;
	}
	public void setfreeForumAvailable(int freeForumAvailable) {
		this.freeForumAvailable = freeForumAvailable;
	}
}

